// - Lógica de programação:

// - Em linhas gerais, lógica de programação é todo conjunto de regras e conceitos que precisamos aplicar para criar códigos que serão interpretados e executados por um computador.

// - Um algoritmo é uma sequência de passos que visa solucionar um problema específico. Eles são utilizados em diversas áreas do conhecimento, como matemática, engenharia, física e, claro, na programação. Em programação, um algoritmo é uma sequência de instruções que pode ser executada por um computador.

// - Para fazer lógica de programação, é preciso desenvolver a capacidade de pensar de forma lógica e estruturada. É importante dominar os conceitos básicos, como dados, variáveis e algoritmos.

// - Para melhorar a lógica de programação, você pode: 
//     - Resolver quebra-cabeças e jogos de lógica.
//     - Desenvolver algoritmos à mão.
//     - Participar de plataformas de codificação competitiva.
//     - Ensinar o que você aprendeu.
//     - Estudar matemática para exercitar o raciocínio lógico.
//     - Entender como os algoritmos funcionam.
//     - Participar de comunidades de programação.
//     - Praticar e elaborar maneiras diferentes para resolver um mesmo problema.

// - Algumas dicas para aprender lógica de programação são: 
//     - Entender e aprender os conceitos básicos.
//     - Decompor o problema a ser resolvido.
//     - Tentar resolver o problema analogicamente antes de programar.
//     - Definir a ordem correta das instruções.
//     - Entender como os algoritmos funcionam.

