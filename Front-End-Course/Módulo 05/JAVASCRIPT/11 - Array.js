// - Array:

// - Array é um objeto especial que armazena uma lista ordenada de elementos. É utilizado para armazenar e acessar múltiplos valores em uma única estrutura. Pode ser criado usando colchetes ([]) e adicionando os elementos separados por vírgula.

// - Exemplo:

    let frutas = ["maça", "banana", "laranja"]

    console.log(frutas[0]) // Retornará maça
    console.log(frutas.length) // Retornará 3

    frutas.push("uva")
    console.log(frutas) // [ 'maça', 'banana', 'laranja', 'uva' ]

// - Nesse exemplo, criamos um array 'frutas' que contém três elementos. Podemos acessar os elementos do array utilizando o índice (começando de 0) e obter o comprimento do array utilizando a propriedade 'lenght'. Também é possível adicionar elementos ao array usando o método 'push()'.