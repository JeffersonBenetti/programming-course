// - Numbers:

// - Em JavaScript, existem vários tipos de números que podem ser usados para representar valores numéricos.

// -' Número inteiro' representa um número inteiro positivo ou negativo, sem casas decimais.

// - Exemplo:

    let numeroInteiro = 10

    console.log(numeroInteiro)
        //// 10

// - 'Número decimal' representa um número com casas decimais, também conhecido como ponto flutuante.

// - Exemplo:

    let numeroDecimal = 3.14

    console.log(numeroDecimal) // 3.14

// - 'Número exponencial' representa um número usando a notação exponencial, com uma base e um expoente. A variável exponencial é usada quando um número é muito grande ou muito pequeno.

// - Exemplo:

    let numeroExponencial = 1.23e+5 // Representa 123000

    console.log(numeroExponencial) // 123000

// - É importante observar que, em JavaScript, os números não são explicitamente definidos como um tipo específico. Eles são todos considerados do tipo 'number' e o JavaScript faz a conversão automaticamente conforme necessário.