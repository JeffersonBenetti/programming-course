// - Operador ternário:

// - O 'operador ternário' em JavaScript é uma forma concisa de escrever uma estrutura condicional com três partes: uma 'expressão condicional', uma 'expressão para o caso' 'verdadeiro' e uma 'expressão para o caso falso'. Ele permite fazer uma escolha entre duas opções com base em uma condição.

// - Exemplo:
    const numero = 7;
    const resultado = numero % 2 === 0 ? 'par' : 'ímpar';

    console.log(resultado); // Output: ímpar

// - Neste exemplo, a expressão 'numero % 2 === 0' verifica se o número é divisível por 2, o que indica que ele é par. Se a condição for verdadeira, o valor 'par' é atribuído à variável 'resultado'; caso contrário, o valor 'ímpar' é atribuído.