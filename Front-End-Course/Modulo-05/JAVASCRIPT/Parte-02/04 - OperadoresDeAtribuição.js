// - Operadores de Atribuição:

// - Os operadores de atribuição no JavaScript são utilizados para atribuir valores a variáveis. Eles permitem atribuir um valor a uma variável existente de forma concisa.

// - Atribuição básica (=): O operador de atribuição básica é utilizado para atribuir um valor a uma variável.

// - Exemplo:
    let a;
    a = 5;
    console.log(a); // Saída: 5


// - Atribuição com adição (+=): O operador de atribuição com adição é utilizado para somar um valor a uma variável existente e atribuir o resultado à variável.

// - Exemplo:
    let b = 5;
    b += 3; // Equivalente a b = b + 3;
    console.log(b); // Saída: 8

// - Atribuição com subtração (-=): O operador de atribuição com subtração é utilizado para subtrair um valor de uma variável existente e atribuir o resultado à variável.

// - Exemplo:
    let c = 5;
    c -= 3; // Equivalente a c = c - 3;
    console.log(c); // Saída: 2

// - Atribuição com multiplicação (*=): O operador de atribuição com multiplicação é utilizado para multiplicar uma variável existente por um valor e atribuir o resultado à variável.

// - Exemplo:
    let d = 5;
    d *= 3; // Equivalente a d = d * 3;
    console.log(d); // Saída: 15


// - Atribuição com divisão (/=): O operador de atribuição com divisão é utilizado para dividir uma variável existente por um valor e atribuir o resultado à variável.

// - Exemplo:
    let e = 10;
    e /= 2; // Equivalente a e = e / 2;
    console.log(x); // Saída: 5

// - Atribuição com resto da divisão (%=): O operador de atribuição com resto da divisão é utilizado para calcular o resto da divisão de uma variável existente por um valor e atribuir o resultado à variável.

// - Exemplo:
    let f = 10;
    f %= 3; // Equivalente a f = f % 3;
    console.log(x); // Saída: 1

// - Esses são alguns dos operadores de atribuição mais comuns do JavaScript. Eles são úteis para atualizar o valor de uma variável de forma concisa, executando uma operação aritmética e atribuindo o resultado diretamente à variável.