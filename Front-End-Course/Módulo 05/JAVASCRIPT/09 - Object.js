// - Object:

// - Imagine que você tenha uma variável chamada pessoa e queira ter uma coleção de propriedades, como nome, idade, cidade... Isso é possível?

// - Claro que é, isso é chamado de objeto. Objeto é uma coleção de propriedades, sendo cada propriedade definida como uma sintaxe de chave: valor. A chave pode ser uma string e o valor pode ser qualquer informação.

// - Exemplo:

    const pessoa = {
        name: 'Stephani',
        age: 27,
    }

    console.log(pessoa) // { name: 'Stephani', age: 27 }